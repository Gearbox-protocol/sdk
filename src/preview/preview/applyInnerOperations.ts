import {
  AbstractAdapterContract,
  ERC4626AdapterContract,
  type SdkWithAdapters,
} from "../../plugins/adapters/index.js";
import {
  type Asset,
  AssetsMap,
  MAX_UINT256,
  type PluginsMap,
} from "../../sdk/index.js";
import type {
  AdapterOperation,
  AddCollateralOp,
  DecreaseDebtOp,
  IncreaseDebtOp,
  InnerOperation,
  WithdrawCollateralOp,
} from "../parse/index.js";
import { applyRWAWrapUnwrap } from "./applyRWAWrapUnwrap.js";
import {
  ERROR_ADAPTER_CALL_OUTSIDE_BRACKET,
  ERROR_MALFORMED_BRACKET,
  ERROR_NON_ADAPTER_CALL_IN_BRACKET,
  ERROR_UNPREVIEWABLE_ADAPTER_CALL,
  ERROR_UNPREVIEWABLE_RWA_WRAP_UNWRAP,
  type OperationPreviewError,
} from "./types.js";

/**
 * Running state threaded through a credit-facade multicall by
 * {@link applyInnerOperations}. Seeded by the caller:
 * - account opening: everything zero/empty (the account doesn't exist yet);
 * - account adjustment: `balances` from the account's current token balances,
 *   `debt` from the principal, `totalDebt` from principal + accrued interest +
 *   accrued fees.
 */
export interface InnerOperationsState {
  /**
   * Cumulative credit-account balances, mutated in facade execution order.
   */
  balances: AssetsMap;
  /**
   * Debt principal
   */
  debt: bigint;
  /**
   * Total debt: principal + accrued interest + accrued fees.
   * `decreaseDebt` amounts operate on total debt (interest and fees are repaid
   * before principal), so principal alone is not enough to resolve either the
   * underlying balance decrement or the final principal.
   */
  totalDebt: bigint;
  /**
   * Cumulative amounts added via `addCollateral`.
   */
  collateralAdded: AssetsMap;
  /**
   * Cumulative amounts withdrawn via `withdrawCollateral`, with `MAX_UINT256`
   * resolved against the running balance at the point of the call.
   */
  collateralWithdrawn: AssetsMap;
  /**
   * Raw `updateQuota` changes, one entry per op. Entries are relative signed
   * changes (with the `MIN_INT96` "disable" sentinel kept as-is), not final
   * quotas: folding them requires pre-state quotas, see `applyQuotaChanges`.
   */
  quotaChanges: Asset[];
}

/**
 * Creates an all-zero {@link InnerOperationsState} (the account-opening seed).
 */
export function makeInnerOperationsState(): InnerOperationsState {
  return {
    balances: new AssetsMap(),
    debt: 0n,
    totalDebt: 0n,
    collateralAdded: new AssetsMap(),
    collateralWithdrawn: new AssetsMap(),
    quotaChanges: [],
  };
}

/**
 * Replays a credit-facade multicall over `state`, mutating it in place in
 * facade execution order. The result is the *minimal guaranteed* post-state.
 *
 * The function assumes that the call was generated by our frontend using
 * router/withdrawal compressor, otherwise it returns an error, but still
 * proceeds best-effort.
 *
 * @returns `undefined` on success, the error on a malformed multicall.
 */
export async function applyInnerOperations<P extends PluginsMap>(
  sdk: SdkWithAdapters<P>,
  multicall: InnerOperation[],
  state: InnerOperationsState,
): Promise<OperationPreviewError | undefined> {
  // True between `StoreExpectedBalances` and `CompareBalances`
  let inBracket = false;
  let storeSeen = false;
  let error: OperationPreviewError | undefined;

  for (const op of multicall) {
    let opError: OperationPreviewError | undefined;

    switch (op.operation) {
      case "AddCollateral":
        applyAddCollateral(state, op);
        break;
      case "WithdrawCollateral":
        applyWithdrawCollateral(state, op);
        break;
      case "IncreaseBorrowedAmount":
        applyIncreaseDebt(state, op);
        break;
      case "DecreaseBorrowedAmount":
        applyDecreaseDebt(state, op);
        break;
      case "UpdateQuota":
        state.quotaChanges.push({ token: op.token, balance: op.change });
        break;
      case "StoreExpectedBalances":
        if (storeSeen) {
          opError = {
            code: ERROR_MALFORMED_BRACKET,
            message: "duplicate storeExpectedBalances call",
          };
        }
        storeSeen = true;
        inBracket = true;
        // StoreExpectedBalances applies deltas to "tokens out"
        for (const { token, balance } of op.deltas) {
          state.balances.inc(token, balance);
        }
        break;
      case "CompareBalances":
        if (!inBracket) {
          opError = {
            code: ERROR_MALFORMED_BRACKET,
            message:
              "compareBalances without a preceding storeExpectedBalances",
          };
        }
        inBracket = false;
        break;
      case "Execute":
        opError = await applyExecute(sdk, op, inBracket, state.balances);
        break;
    }

    // save error, but continue
    error ??= opError;
  }

  if (inBracket) {
    // The router always emits storeExpectedBalances/compareBalances as a pair
    error ??= {
      code: ERROR_MALFORMED_BRACKET,
      message: "storeExpectedBalances without a matching compareBalances",
    };
  }

  return error;
}

function applyAddCollateral(
  state: InnerOperationsState,
  op: AddCollateralOp,
): void {
  state.collateralAdded.inc(op.token, op.amount);
  state.balances.inc(op.token, op.amount);
}

/**
 * `MAX_UINT256` is the facade sentinel for "withdraw the entire balance", so
 * it is resolved against the running balance at the point of the call.
 */
function applyWithdrawCollateral(
  state: InnerOperationsState,
  op: WithdrawCollateralOp,
): void {
  const running = state.balances.getOrZero(op.token);
  const amount =
    op.amount === MAX_UINT256 ? (running > 0n ? running : 0n) : op.amount;
  state.balances.dec(op.token, amount);
  state.collateralWithdrawn.inc(op.token, amount);
}

function applyIncreaseDebt(
  state: InnerOperationsState,
  op: IncreaseDebtOp,
): void {
  state.debt += op.amount;
  state.totalDebt += op.amount;
  state.balances.inc(op.token, op.amount);
}

/**
 * The facade clamps the repaid amount to the total debt (principal + accrued
 * interest + fees); `MAX_UINT256` means "repay everything". Interest and fees
 * are repaid before principal (CreditLogic.calcDecrease), so principal only
 * decreases once total debt drops below it.
 */
function applyDecreaseDebt(
  state: InnerOperationsState,
  op: DecreaseDebtOp,
): void {
  const repaid = op.amount > state.totalDebt ? state.totalDebt : op.amount;
  state.balances.dec(op.token, repaid);
  state.totalDebt -= repaid;
  if (state.debt > state.totalDebt) {
    state.debt = state.totalDebt;
  }
}

/**
 * Replays an adapter `Execute` call against the running balances.
 *
 * Inside a bracket, the adapter's balance changes are previewed via
 * {@link AbstractAdapterContract.previewBalanceChanges}. Outside a bracket,
 * only RWA wrap/unwrap calls are allowed (see {@link applyRWAWrapUnwrap});
 * nothing enforces the outcome of any other out-of-bracket adapter call
 * on-chain, so its effect on balances cannot be previewed.
 */
async function applyExecute<P extends PluginsMap>(
  sdk: SdkWithAdapters<P>,
  op: AdapterOperation,
  inBracket: boolean,
  balances: AssetsMap,
): Promise<OperationPreviewError | undefined> {
  const adapter = sdk.getContract(op.adapter);

  if (!inBracket) {
    if (!isRWAShare(sdk, adapter)) {
      return {
        code: ERROR_ADAPTER_CALL_OUTSIDE_BRACKET,
        message: `call to ${op.adapter} outside of a storeExpectedBalances/compareBalances bracket`,
      };
    }
    try {
      applyRWAWrapUnwrap(adapter, op.calldata, balances);
      return undefined;
    } catch (e) {
      // decodeFunctionData throws on calldata that doesn't match the
      // ERC4626 adapter ABI
      return {
        code: ERROR_UNPREVIEWABLE_RWA_WRAP_UNWRAP,
        message: e instanceof Error ? e.message : String(e),
      };
    }
  }

  // unknown adapter or Execute call to some contract that is genuinely not an adapter
  if (!(adapter instanceof AbstractAdapterContract)) {
    return {
      code: ERROR_NON_ADAPTER_CALL_IN_BRACKET,
      message: `call to ${op.adapter} between storeExpectedBalances and compareBalances is not an adapter call`,
    };
  }

  try {
    await adapter.previewBalanceChanges(balances, op.calldata);
    return undefined;
  } catch (e) {
    return {
      code: ERROR_UNPREVIEWABLE_ADAPTER_CALL,
      message: e instanceof Error ? e.message : String(e),
    };
  }
}

/**
 * True when the ERC4626 adapter converts an RWA underlying, i.e. it is the
 * wrap/unwrap adapter of an RWA market rather than a regular vault strategy
 * adapter.
 */
function isRWAShare<P extends PluginsMap>(
  sdk: SdkWithAdapters<P>,
  adapter: unknown,
): adapter is ERC4626AdapterContract {
  if (adapter instanceof ERC4626AdapterContract) {
    const meta = sdk.tokensMeta.get(adapter.share);
    return !!meta && sdk.tokensMeta.isRWAUnderlying(meta);
  }
  return false;
}
