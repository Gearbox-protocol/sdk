import { AbstractAdapterContract } from "../../plugins/adapters/index.js";
import {
  type AddressMap,
  AP_WETH_TOKEN,
  type Asset,
  AssetsMap,
  NO_VERSION,
  type PluginsMap,
} from "../../sdk/index.js";
import type {
  InnerOperation,
  OpenCreditAccountOperation,
  SecuritizeOpenCreditAccountOperation,
} from "../parse/index.js";
import type {
  OpenCreditAccountPreview,
  PreviewOperationInput,
} from "./types.js";
import { unwrapNativeCollateral } from "./unwrapNativeCollateral.js";

export function previewOpenCreditAccount<P extends PluginsMap>(
  input: PreviewOperationInput<P>,
  operation: OpenCreditAccountOperation | SecuritizeOpenCreditAccountOperation,
): OpenCreditAccountPreview {
  const { sdk, value = 0n } = input;
  const market = sdk.marketRegister.findByCreditManager(
    operation.creditManager,
  );

  const collateralBalances = new AssetsMap();
  // Cumulative credit-account balances, mutated in facade execution order.
  // Since we open an account, initial balances are all zero.
  const balances = new AssetsMap();
  let debt = 0n;
  const quotas: Asset[] = [];
  // True between `StoreExpectedBalances` and `CompareBalances`. Every
  // router-generated call inside the bracket is diff-style: it spends the
  // consumed token down to the exact leftover encoded in its calldata, so
  // threading balances through each adapter yields the final leftovers of
  // all consumed tokens.
  let inBracket = false;
  let storeSeen = false;

  for (const op of operation.multicall) {
    switch (op.operation) {
      case "AddCollateral":
        collateralBalances.inc(op.token, op.amount);
        balances.inc(op.token, op.amount);
        break;
      case "WithdrawCollateral":
        balances.dec(op.token, op.amount);
        break;
      case "IncreaseBorrowedAmount":
        debt += op.amount;
        balances.inc(op.token, op.amount);
        break;
      case "DecreaseBorrowedAmount":
        debt -= op.amount;
        balances.dec(op.token, op.amount);
        break;
      case "UpdateQuota":
        quotas.push({ token: op.token, balance: op.change });
        break;
      case "StoreExpectedBalances":
        if (storeSeen) {
          throw new Error(
            "malformed multicall: duplicate storeExpectedBalances call",
          );
        }
        storeSeen = true;
        inBracket = true;
        // Deltas are applied directly to the running balances. This assumes
        // the multicall was generated by the router's `routeOpenManyToOne`,
        // whose `updateSlippageControl` emits a single `BalanceDelta` for the
        // target token, and the target is never consumed as an adapter input
        // inside the bracket (adapters only overwrite input leftovers), so
        // nothing can clobber the applied delta.
        for (const { token, balance } of op.deltas) {
          balances.inc(token, balance);
        }
        break;
      case "CompareBalances":
        if (!inBracket) {
          throw new Error(
            "malformed multicall: compareBalances without a preceding storeExpectedBalances",
          );
        }
        // Purely a validation marker: on-chain, compareBalances only asserts
        // that stored minimums are met and changes no balances.
        inBracket = false;
        break;
      case "Execute":
        if (inBracket) {
          const adapter = sdk.getContract(op.adapter);
          if (!(adapter instanceof AbstractAdapterContract)) {
            throw new Error(
              `call to ${op.adapter} between storeExpectedBalances and compareBalances is not an adapter call`,
            );
          }
          adapter.previewBalanceChanges(balances, op.calldata);
        }
        break;
    }
  }
  if (inBracket) {
    // The router always emits storeExpectedBalances/compareBalances as a pair
    throw new Error(
      "malformed multicall: storeExpectedBalances without a matching compareBalances",
    );
  }

  let collateral: Asset[] = collateralBalances.toAssets();
  // collateral value is computed before unwrapping since the oracle cannot
  // price the native token
  const collateralValue = collateral.reduce(
    (acc, { token, balance }) =>
      acc + market.priceOracle.convert(token, market.underlying, balance),
    0n,
  );
  collateral = unwrapNativeCollateral(
    collateral,
    value,
    sdk.addressProvider.getAddress(AP_WETH_TOKEN, NO_VERSION),
  );

  // filter out dust, including the 1-wei leftovers of drained inputs and
  // intermediate tokens
  const assets: Asset[] = balances
    .entries()
    .filter(([, balance]) => balance > 1n)
    .map(([token, balance]) => ({ token, balance }));

  return {
    operation: operation.operation,
    creditManager: operation.creditManager,
    target: inferTargetAsset(operation.multicall, balances),
    collateral,
    collateralValue,
    debt,
    quotas,
    assets,
  };
}

/**
 * Infers the strategy target token of an account opening: the first quoted
 * token (first `UpdateQuota` with a positive change), with its balance taken
 * from the computed assets. Returns `undefined` when nothing is quoted.
 */
function inferTargetAsset(
  multicall: InnerOperation[],
  balances: AddressMap<bigint>,
): Asset | undefined {
  for (const op of multicall) {
    if (op.operation === "UpdateQuota" && op.change > 0n) {
      const balance = balances.get(op.token);
      return balance ? { token: op.token, balance } : undefined;
    }
  }
  return undefined;
}
